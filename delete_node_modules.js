var fs = require("fs");
var path = require("path");
var rm = require("rimraf");
const { readdirSync, stat } = fs;
var deleteFile = function (src, callbak) {
  let paths = readdirSync(src); //同步读取当前⽬录
  paths.forEach(function (path) {
    var _src = src + "/" + path;
    stat(_src, function (err, stats) {
      //stats 该对象包含⽂件属性
      if (err) {
        console.log("err=", err);
        throw err;
      }
      if (stats.isFile()) {
        //如果是个⽂件则拷贝
        // let readable = fs.createReadStream(_src); //创建读取流
        // let writable = fs.createWriteStream(_dst); //创建写⼊流
        // readable.pipe(writable);
        // callback()
        rm(_src, () => {
          console.log("成功删除文件:", _src);
        });
        
      } else if (stats.isDirectory()) {
        // callbak(_src, path, callbak);
        deleteFile(_src, callbak);
      }
    });
  });
};

